#Formatting Examples

print("The age of {0} is {1}".format('Jim', 23))

print("The age of {0} is {1}. {0}'s birthday is on {2}".format('Brian', 24, 'Oct 21'))

print("Reticulating spline of {} of {}".format(4,23))
print("Current position {latitude} {longitude}".format(latitude="60N", longitude="5E"))
print("Galactic Position x={pos[0]}, y={pos[1]}, z={pos[2]}".format(pos=(5.2,4.5,6.3)))

#print("Math constants: pi={m.pi:.3f}, e={m.e:.3f}".format(m=Math))

value = 4*20
print("The value is {0}".format(value))

