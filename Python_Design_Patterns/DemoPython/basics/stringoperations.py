s="You are awesome"
print(s)

#Repetition
print(s*3)

#Indexing
print(s[0])

print(len(s))

s1="""You are
the creator 
of your own destiny"""
print(s1)

#Slicing A String

print(s[0:5])
print(s[0:])
print(s[:8])

#-1 holds the last index value
print(s[-3:-1])


print(s[0:9:2])
print(len(s))
print(s[15::-1])

#Reversing a String
"""First : - Starting Point,
Second : Ending Point,
Last Colon : Number Of Jumps [STEP]"""

print(s[::-1])

